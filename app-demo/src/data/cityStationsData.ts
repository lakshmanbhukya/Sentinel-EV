// Generate mock stations from coordinates
function generateStationsForCity(cityName: string): Station[] {
  const coords = CITY_STATION_COORDS[cityName] || [];
  const statuses: Array<'safe' | 'warning' | 'critical'> = ['safe', 'warning', 'critical'];
  
  return coords.map((coord, index) => {
    const status = statuses[Math.floor(Math.random() * statuses.length)];
    const load = Math.floor(Math.random() * 60) + 20; // 20-80%
    const temp = Math.floor(Math.random() * 40) + 45; // 45-85°C
    
    return {
      id: `${cityName.toLowerCase().replace(/\s+/g, '-')}-${index + 1}`,
      name: `${cityName} EV Station ${index + 1}`,
      lat: coord.lat,
      lng: coord.lng,
      status,
      load,
      temp,
      address: `Station ${index + 1}, ${cityName}`,
    };
  });
}

